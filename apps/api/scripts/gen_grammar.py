def esc(s): return s.replace("'","''")

def main():
    out = os.path.join(os.path.dirname(__file__),'..','migrations','0014_grammar_seed.sql')
    lines = ["-- Migration: 0014_grammar_seed.sql","-- HSK 1 Grammar lessons seed data\n"]
    
    for (oid,tmn,tzh,gp,pat,expl,examples,exercises) in GRAMMAR:
        ex_json = json.dumps([{"zh":z,"pinyin":p,"mn":m} for z,p,m in examples], ensure_ascii=False)
        lines.append(
            f"INSERT INTO grammar_lessons (id,title_mn,title_zh,grammar_point,explanation_mn,pattern,examples,hsk_level,order_num) "
            f"VALUES ({oid},'{esc(tmn)}','{esc(tzh)}','{esc(gp)}','{esc(expl)}','{esc(pat)}',"
            f"'{esc(ex_json)}',1,{oid});"
        )
    
    lines.append("")
    ex_id = 1
    for (oid,_,_,_,_,_,_,exercises) in GRAMMAR:
        for i,(etype,q,ans,opts,expl) in enumerate(exercises):
            lines.append(
                f"INSERT INTO grammar_exercises (id,grammar_lesson_id,exercise_type,question_zh,correct_answer,options,explanation_mn,order_num) "
                f"VALUES ({ex_id},{oid},'{etype}','{esc(q)}','{esc(ans)}','{esc(opts)}','{esc(expl)}',{i+1});"
            )
            ex_id += 1
    
    with open(out,'w') as f: f.write('\n'.join(lines))
    print(f"Generated: {out}")
    print(f"Grammar lessons: {len(GRAMMAR)}, Exercises: {ex_id-1}")

if __name__=='__main__': main()
